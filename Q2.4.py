letra = input("Insira a letra desejada: ")

if letra == "a":
	print ("Essa letra é uma vogal!")
elif letra == e:
	print ("Essa letra é uma vogal!")
elif letra == i:
	print ("Essa letra é uma vogal!")
elif letra == o:
	print ("Essa letra é uma vogal!")
elif letra == u:
	print ("Essa letra é uma vogal!")
else:
	print ("Essa letra é uma consoante!")

